our $VERSION = "0.114";

use Test::Engine load => __PACKAGE__;

1;

__END__

=pod

=encoding UTF-8

=head1 NAME

Test::Tester - Ease testing test modules built with Test::Builder

=head1 DESCRIPTION

If you have written a test module based on Test::Builder then Test::Tester
allows you to test it with the minimum of effort.

See L<Test::Tester::Legacy> for the original documentation and implementation
of this library. If you decide to use an engine other than the default 'Legacy'
you should see that engines documentation as it may change things.

=head1 SEE ALSO

=over 4

=item L<Test::Engine>

Details about the new pluggable engine system.

=item L<Test::Tester::Legacy>

The default and original Test::Tester.

=back

